let firstFavNum = 14;
let secondFavNum = 5;

let sum = firstFavNum  + secondFavNum;
console.log(firstFavNum + " + " + secondFavNum + " = " + sum);
//output: 14 + 5 = 19

let difference = firstFavNum  -  secondFavNum;
console.log(firstFavNum + " - " + secondFavNum + " = " + difference);
//output: 14 - 5 = 9

let product = firstFavNum  * secondFavNum;
console.log(firstFavNum + " x " + secondFavNum + " = " + product);
//output: 14 x 5 = 70

let quotient = firstFavNum / secondFavNum;
console.log(firstFavNum + " รท " + secondFavNum + " = " + quotient);
//output: 14 รท 5 = 2.8

let modulus = firstFavNum % secondFavNum;
console.log(firstFavNum + " % " + secondFavNum + " = " + modulus);
//output: 14 % 5 = 4

let exponentiation = firstFavNum ** secondFavNum;
console.log(firstFavNum + " ^ " + secondFavNum + " = " + exponentiation);
//output: 14 ^ 5 = 537824

